#include <iostream>

int main(int argc, const char* argv[]) {
  int integer_number = 10;
  float decimal_number = 5.5;
  bool is_true = true;
  char character = 'a';

  std::cout << "Integer number: " << integer_number << std::endl;
  std::cout << "Decimal number: " << decimal_number << std::endl;
  std::cout << "Is true: " << is_true << std::endl;
  std::cout << "Character: " << character << std::endl;

  return 0;
}